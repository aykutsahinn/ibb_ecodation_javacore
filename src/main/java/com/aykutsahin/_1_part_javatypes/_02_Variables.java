package com.aykutsahin._1_part_javatypes;

public class _02_Variables {
    public static void main(String[] args) {

        int schoolNumber = 25;
        System.out.println(schoolNumber);

        schoolNumber = 44;
        System.out.println(schoolNumber);

        // değişken isimleri _ veya $ ile başlayabilir
        int _schoolNumber = 85;
        System.out.println(_schoolNumber);

        int $schoolNumber = 75;
        System.out.println($schoolNumber);

        // yazmayalım
        float totalSalary = 14.5355f;
        System.out.println(totalSalary);

        // SABIT
        final int MAX_STUDENTS = 100;
        System.out.println(MAX_STUDENTS);
    }
}
